create trigger actualizarMontoVenta
on detalleventa
for insert
as
begin
    -- Declaramos variables locales para almacenar el ID de la venta y el nuevo monto total
    declare @id_venta int;

    -- Obtenemos el id_venta del registro insertado
    select @id_venta = id_venta from inserted;

    -- Calculamos el nuevo monto sumando todos los precios en detalleventa para la misma venta
    declare @nuevoMonto float;
    select @nuevoMonto = i.cantidad * i.preciov 
    from inserted i;

    -- Actualizamos el monto en la tabla notaventa
    update notaventa
    set monto = @nuevoMonto
    where id_venta = @id_venta;
end;

