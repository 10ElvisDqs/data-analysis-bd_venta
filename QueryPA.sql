create procedure pa_generarDetalleVenta
as
begin
    declare @id_venta int;
    declare @id_producto int;
    declare @cantidad int;
    declare @preciov float;

    declare @ventas int = (select count(*) from notaventa);
    declare @i int = 1;

    -- Iteramos sobre cada nota de venta
    while @i <= @ventas
    begin
        set @id_venta = @i;  -- Asumimos que los id_venta son secuenciales de 1 a @ventas

        -- Generamos un número aleatorio de productos para la nota de venta (de 1 a 30)
        declare @numProductos int = floor(1 + (rand() * 30));

        declare @j int = 1;
        while @j <= @numProductos
        begin
            -- Seleccionar un id_producto aleatorio entre 1 y 200
            set @id_producto = floor(1 + (rand() * 200));

            -- Obtener el precio del producto seleccionado
            select @preciov = precio from producto where id_producto = @id_producto;

            -- Generar una cantidad aleatoria entre 1 y 10
            set @cantidad = floor(1 + (rand() * 10));

            -- Insertar el detalle de venta en la tabla detalleventa
            insert into detalleventa (id_venta, id_producto, cantidad, preciov)
            values (@id_venta, @id_producto, @cantidad, @preciov);

            set @j = @j + 1; -- Incrementar el contador de productos
        end

        SET @i = @i + 1; -- Incrementar el contador de notas de venta
    end
end;

