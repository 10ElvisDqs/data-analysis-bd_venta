create procedure pa_generarNotaVentas
as
begin
    declare @i int = 1;
    declare @fechaActual date = '2021-01-01'; -- Fecha de inicio
    declare @montoAleatorio float;
    declare @idClienteAleatorio int;
    declare @ventasPorDia int;

    while @i <= 5000
    begin
        -- Generar un número aleatorio de ventas para el día actual (de 0 a 5 ventas)
        set @ventasPorDia = floor(rand() * 20); -- Esto genera de 0 a 20 ventas por día

        if @ventasPorDia > 0
        begin
            declare @j int = 1;
            while @j <= @ventasPorDia and @i <= 5000
            begin
                set @montoAleatorio = 0;
				select (1 + (rand()*100))
                -- Seleccionar un id_cliente aleatorio entre 1 y 100
                set @idClienteAleatorio = floor(1 + (rand() * 200));

                -- Insertar la venta generada en la tabla notaventa
                insert into notaventa (id_venta, fecha, monto, id_cliente)
                values (@i, @fechaActual, @montoAleatorio, @idClienteAleatorio);

                -- Incrementar el contador de ventas totales
                set @i = @i + 1;

                -- Incrementar el contador de ventas del día
                set @j = @j + 1;
            end
        end

        -- Avanzar a la siguiente fecha (a veces saltando días 2 o 1)
        set @fechaActual = dateadd(day,floor(1 + (rand() * 2)), @fechaActual); 
        -- Esto incrementa la fecha entre 1 y 2 días aleatoriamente
    end
end;

